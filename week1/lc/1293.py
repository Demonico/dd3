# 1293. Shortest Path in a Grid with Obstacles Elimination
# https://leetcode.com/problems/shortest-path-in-a-grid-with-obstacles-elimination/description/
from collections import deque
from typing import List, Tuple

from common import ExampleInput, execution_time, get_4_neighbors


class Solution:
    @staticmethod
    @execution_time
    def shortest_path(grid: List[List[int]], k: int) -> int:
        num_rows, num_cols = len(grid), len(grid[0])
        # (row, col, steps, remaining eliminations)
        queue = deque([(0, 0, 0, k)])
        # noinspection PySetFunctionToLiteral
        visited: set[Tuple[int, int, int]] = set((0, 0, k))

        while queue:
            r, c, steps, remaining_k = queue.popleft()
            if remaining_k < 0 or (r, c, remaining_k) in visited:
                continue
            # print(r, c, steps, remaining_k)

            if r == num_rows - 1 and c == num_cols - 1:
                return steps
            visited.add((r, c, remaining_k))
            nrk = remaining_k - grid[r][c]
            for nr, nc in get_4_neighbors(grid, (r, c)):
                queue.append((nr, nc, steps + 1, nrk))
        return -1


